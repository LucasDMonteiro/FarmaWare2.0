    public SoldView(List<Sold> soldList) {
        initComponents();
        this.setLocationRelativeTo(null);
        
        DefaultTableModel tableModel = (DefaultTableModel)tblSolds.getModel();
        
        for (Sold sold :  soldList){
            Saleable saleable = new Saleable(sold.getSaleableId());
            
            SaleableDAO dao = new SaleableDAO();
            try {
                saleable = dao.search(saleable);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
                return;
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
                return;
            }
            
            Object[] values = new Object[]{saleable.getId(), saleable.getName(), saleable.getPrice(), sold.getQty()};
            tableModel.addRow(values);
        }
    }

