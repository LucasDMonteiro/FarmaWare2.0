    public SaleableView(Saleable saleable, byte categ) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.saleable = saleable;
        this.categ = categ;
        
        if(saleable != null){
            if(categ == Saleable.PRODUCT)
                lblAction.setText("Alterar Produto");
            if(categ == Saleable.DRUG)
                lblAction.setText("Alterar Medicamento");
            
            btnAction.setText("Alterar");
            
            try {
                saleable = new SaleableDAO().search(saleable);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
            }
            
            txtName.setText(saleable.getName());
            txtDesc.setText(saleable.getDesc());
            txtManufc.setText(saleable.getManufc());
            txtPrice.setText(String.valueOf(saleable.getPrice()));
            spnStock.setValue(saleable.getStock());
        }
        
        if(categ == Saleable.PRODUCT)
            lblManufc.setText("Fabricante");
        else if(categ == Saleable.DRUG)
            lblManufc.setText("Laboratório");
    }

