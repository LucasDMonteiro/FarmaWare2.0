    private void btnActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionActionPerformed
        String name = txtName.getText();
        String desc = txtDesc.getText();
        String manufc = txtManufc.getText();
        String price = txtPrice.getText();
        
        int stock = 0;
        try {
            spnStock.commitEdit();
            stock = (Integer)spnStock.getValue();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Insira apenas números para a quantidade em estoque!", "Atenção", 0);
        }
        
        if(name.equals("")){
            JOptionPane.showMessageDialog(this, "Insira um nome válido!", "Atenção", 0);
            return;
        }
        
        if(manufc.equals("") || manufc.matches("\\d*")){
            if(saleable.getCateg() == Saleable.PRODUCT)
                JOptionPane.showMessageDialog(this, "Insira uma fabricante válido!", "Atenção", 0);
            if(saleable.getCateg() == Saleable.DRUG)
                JOptionPane.showMessageDialog(this, "Insira um laboratório válido!", "Atenção", 0);
            return;
        }
        
        if(price.equals("") || !price.matches("^[0-9,.]*$")){
            JOptionPane.showMessageDialog(this, "Insira um preço válido!", "Atenção", 0);
            return;
        }
        
        
        boolean success = false;
        SaleableDAO dao = new SaleableDAO();
        Saleable currentProduct = new Saleable(name, desc, manufc, Double.parseDouble(price), stock, categ);
        
        if(saleable == null){
            // Insert
            try {
                success = dao.insert(currentProduct);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
                return;
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
                return;
            }
        }
        else{
            try {
                success = dao.update(currentProduct);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
                return;
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
                return;
            }
        }
        
        if(success){
            JOptionPane.showMessageDialog(this, (saleable == null ? "Gravado" : "Alterado") + " com sucesso!", "Sucesso", 1);
            this.dispose();
        }
        else
            JOptionPane.showMessageDialog(this, "Erro ao " + (saleable == null ? "gravar" : "alterar") + "! Tente novamente!", "Erro", 0);
    }//GEN-LAST:event_btnActionActionPerformed

