    private void btnActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionActionPerformed
        if(lstEntities.getSelectedIndex() < 0){
            JOptionPane.showMessageDialog(this, "Selecione um valor da lista", "Atenção", 0);
            return;
        }
        
        String value = lstEntities.getSelectedValue();
        
        System.out.println("VALUE: " + value);
        
        switch(entity){
            case CLIENT:
                String cpf = value.split(":")[0];
                ClientDAO cDao = new ClientDAO();
                Client client = new Client(cpf);
                try{
                    client = cDao.search(client);
                    this.selectedEntity = client;
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
                    return;
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
                    return;
                }
            break;
            case PRODUCT:
                SaleableDAO sDao = new SaleableDAO();
                Saleable saleable;
                try{
                    saleable = sDao.getRecords("name = '" + value + "'").get(0);
                    this.selectedEntity = saleable;
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
                    return;
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
                    return;
                }
            break;
        }
        
        this.setVisible(false);
    }//GEN-LAST:event_btnActionActionPerformed

