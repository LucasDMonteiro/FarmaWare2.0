    public ChooserView(byte entity) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.entity = entity;
        
        loadData();
        
        String title = "";
        
        switch(entity){
            case CLIENT: title = "Selecionar Cliente"; break;
            case PRODUCT: title = "Selecionar Produto"; break;
        }
        
        this.setTitle(title);
        lblAction.setText(title);
        
        lstEntities.setModel(listModel);
    }
    
    private void loadData(){ 
        listModel.clear();
        DAO dao;
        switch(entity){
            case CLIENT: 
                dao = new ClientDAO();
                
                try {
                    entityList = dao.getRecords("");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
                    return;
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
                    return;
                }
                
                for(Object obj : entityList){
                    Client client = (Client)obj;
                    listModel.addElement(client.getCpf() + " : " +  client.getName());
                }
            break;
            case PRODUCT: 
                dao = new SaleableDAO();
                
                try {
                    entityList = dao.getRecords("stock > 0");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de SQL: \n" + ex.getMessage(), "Erro", 0);
                    return;
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Erro de Classe: \n" + ex.getMessage(), "Erro", 0);
                    return;
                }
                
                for(Object obj : entityList){
                    Saleable product = (Saleable)obj;
                    listModel.addElement(product.getName());
                }
            break;
        }
    }

